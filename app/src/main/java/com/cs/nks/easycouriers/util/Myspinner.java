package com.cs.nks.easycouriers.util;

public class Myspinner {
    String spinnerText;
    String spinnerVal;


    public Myspinner(String spinnerText, String spinnerVal) {
        this.spinnerText = spinnerText;
        this.spinnerVal = spinnerVal;

    }

    public String getSpinnerText() {
        return spinnerText;
    }


    public String getspinnerVal() {
        return spinnerVal;
    }


    public String toString() {
        return spinnerText;
    }


}
